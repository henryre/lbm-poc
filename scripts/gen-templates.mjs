// Codegen: embed the Jinja templates (cli/templates/*.j2) as verbatim TS string
// constants in src/templates.ts, so the TS rendering route uses the exact same
// template text as the Python CLI route — no runtime file I/O (bundler-safe on
// Vercel). The .j2 files remain the single source of truth; this regenerates on
// every build (see package.json "build").
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, "..");
const templateDir = join(repoRoot, "cli", "templates");
const outFile = join(repoRoot, "src", "templates.ts");

// Map template file name -> output relative path produced by `lbm init`.
const TEMPLATES = [
  { file: "lbm.toml.j2", export: "LBM_TOML", path: "lbm.toml" },
  { file: "lbm-dispatch.yml.j2", export: "LBM_DISPATCH", path: ".github/workflows/lbm-dispatch.yml" },
  { file: "lbm-comments.yml.j2", export: "LBM_COMMENTS", path: ".github/workflows/lbm-comments.yml" },
  { file: "lbm-agents.yml.j2", export: "LBM_AGENTS", path: ".github/workflows/lbm-agents.yml" },
  { file: "lbm-ci-hooks.yml.j2", export: "LBM_CI_HOOKS", path: ".github/workflows/lbm-ci-hooks.yml" },
  { file: "AGENTS.md.j2", export: "AGENTS_MD", path: "AGENTS.md" },
];

function escapeForTemplateLiteral(text) {
  // Escape backslash first, then backtick, then `${` (template-literal interpolation).
  return text
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\$\{/g, "\\${");
}

let out = "// AUTO-GENERATED by scripts/gen-templates.mjs — do not edit by hand.\n";
out += "// Source of truth: cli/templates/*.j2 (rendered identically by the Python CLI).\n\n";
out += "export interface TemplateSpec {\n  /** Output path written by `lbm init`. */\n  path: string;\n  /** Verbatim Jinja template text. */\n  source: string;\n}\n\n";

for (const t of TEMPLATES) {
  const raw = readFileSync(join(templateDir, t.file), "utf8");
  out += `export const ${t.export}: TemplateSpec = {\n`;
  out += `  path: ${JSON.stringify(t.path)},\n`;
  out += `  source: \`${escapeForTemplateLiteral(raw)}\`,\n`;
  out += `};\n\n`;
}

out += "/** Templates in the order `lbm init` writes them. */\n";
out += "export const ALL_TEMPLATES: TemplateSpec[] = [\n";
out += "  LBM_DISPATCH,\n  LBM_COMMENTS,\n  LBM_AGENTS,\n  LBM_CI_HOOKS,\n  LBM_TOML,\n  AGENTS_MD,\n];\n";

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, out);
console.log(`Wrote ${outFile} (${TEMPLATES.length} templates)`);
